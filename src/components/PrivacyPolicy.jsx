import { useState, useEffect } from 'react'

// 푸터·문의 폼 양쪽에서 재사용하는 개인정보처리방침 모달.
// trigger(라벨/스타일)만 다르게 주고 내용은 공유한다.
export default function PrivacyPolicy({ className = '', label = '개인정보처리방침' }) {
  const [open, setOpen] = useState(false)

  useEffect(() => {
    if (!open) return
    const onKey = (e) => { if (e.key === 'Escape') setOpen(false) }
    document.addEventListener('keydown', onKey)
    document.body.style.overflow = 'hidden'
    return () => {
      document.removeEventListener('keydown', onKey)
      document.body.style.overflow = ''
    }
  }, [open])

  return (
    <>
      <button type="button" className={className} onClick={() => setOpen(true)}>
        {label}
      </button>

      {open && (
        <div className="privacy-overlay" onClick={() => setOpen(false)}>
          <div className="privacy-modal" role="dialog" aria-modal="true" aria-label="개인정보처리방침" onClick={(e) => e.stopPropagation()}>
            <div className="privacy-head">
              <h4 className="privacy-title">개인정보처리방침</h4>
              <button type="button" className="privacy-close" aria-label="닫기" onClick={() => setOpen(false)}>×</button>
            </div>
            <div className="privacy-body">
              <p>
                포커스특공대(이하 ‘회사’라 한다)는 개인정보 보호법 제30조에 따라 정보주체의 개인정보를 보호하고 이와 관련한 고충을
                신속하고 원활하게 처리할 수 있도록 하기 위하여 다음과 같이 개인정보 처리방침을 수립·공개합니다.
              </p>

              <h5>제1조 (개인정보의 처리목적)</h5>
              <p>
                회사는 다음의 목적을 위하여 개인정보를 처리합니다. 처리하고 있는 개인정보는 다음의 목적 이외의 용도로는 이용되지 않으며,
                이용 목적이 변경되는 경우에는 개인정보 보호법 제18조에 따라 별도의 동의를 받는 등 필요한 조치를 이행할 예정입니다.
              </p>
              <p>1. 고객 문의 접수 및 답변 처리 — 상담 신청 확인, 문의 내용에 대한 답변 및 연락, 상담 진행을 위한 소통을 목적으로 개인정보를 처리합니다.</p>

              <h5>제2조 (수집하는 개인정보 항목)</h5>
              <p>회사는 상담 신청(문의 폼)을 통해 다음의 개인정보 항목을 수집합니다.</p>
              <p>
                필수항목 : 회사명, 담당자명(이름), 이메일 주소, 연락처, 문의 내용<br />
                자동 수집 항목 : 서비스 이용 과정에서 IP주소, 쿠키, 접속 기록 등이 자동으로 생성되어 수집될 수 있습니다.
              </p>

              <h5>제3조 (개인정보의 보유 및 이용 기간)</h5>
              <p>
                회사는 정보주체로부터 개인정보를 수집할 때 동의받은 보유·이용 기간 내에서 개인정보를 처리·보유합니다.
                수집한 개인정보는 <strong>문의 처리 완료 후 즉시 파기</strong>하는 것을 원칙으로 합니다. 다만 관계 법령에 따라
                보존할 필요가 있는 경우 해당 법령에서 정한 기간 동안 보관합니다.
              </p>

              <h5>제4조 (개인정보 처리의 위탁)</h5>
              <p>회사는 원활한 개인정보 업무 처리를 위하여 다음과 같이 개인정보 처리업무를 위탁하고 있습니다.</p>
              <p>
                - 수탁업체 : EmailJS (EmailJS, Inc.)<br />
                - 위탁 업무 내용 : 문의 서비스 제공을 위한 이메일 발송 시스템 연동 및 문의 내용 전송<br />
                - 위탁 기간 : 위탁 목적 달성 후 즉시 파기 (회원 탈퇴 또는 위탁 계약 종료 시까지)
              </p>
              <p>
                회사는 위탁계약 체결 시 개인정보 보호법에 따라 위탁업무 수행목적 외 개인정보 처리 금지, 기술적·관리적 보호조치,
                재위탁 제한, 수탁자에 대한 관리·감독, 손해배상 등 책임에 관한 사항을 계약서 등 문서에 명시하고, 수탁자가 개인정보를
                안전하게 처리하는지를 감독하고 있습니다. 위탁업무의 내용이나 수탁자가 변경될 경우에는 지체없이 본 방침을 통해 공개하겠습니다.
              </p>

              <h5>제5조 (정보주체와 법정대리인의 권리·의무 및 행사방법)</h5>
              <p>① 정보주체는 회사에 대해 언제든지 다음 각 호의 개인정보 보호 관련 권리를 행사할 수 있습니다.</p>
              <p>1. 개인정보 열람 요구<br />2. 오류 등이 있을 경우 정정 요구<br />3. 삭제 요구<br />4. 처리정지 요구</p>
              <p>
                ② 제1항에 따른 권리 행사는 회사에 대해 서면, 전자우편 등을 통하여 하실 수 있으며 회사는 이에 대해 지체없이 조치하겠습니다.
                ③ 정보주체가 개인정보의 오류 등에 대한 정정 또는 삭제를 요구한 경우, 회사는 정정 또는 삭제를 완료할 때까지 해당 개인정보를
                이용하거나 제공하지 않습니다.
              </p>

              <h5>제6조 (개인정보의 파기)</h5>
              <p>
                ① 회사는 개인정보 보유 기간의 경과, 처리목적 달성 등 개인정보가 불필요하게 되었을 때에는 지체없이 해당 개인정보를 파기합니다.
                ② 전자적 파일 형태의 개인정보는 기록을 재생할 수 없도록 안전한 방법으로 삭제하며, 종이 문서에 기록된 개인정보는 분쇄하거나 소각하여 파기합니다.
              </p>

              <h5>제7조 (개인정보의 안전성 확보조치)</h5>
              <p>
                회사는 개인정보의 안전성 확보를 위해 다음과 같은 조치를 하고 있습니다.<br />
                1. 관리적 조치 : 내부관리계획 수립 및 시행, 접근 권한 최소화 등<br />
                2. 기술적 조치 : 개인정보처리시스템 접근 권한 관리, 전송 구간 암호화(HTTPS) 등<br />
                3. 물리적 조치 : 개인정보가 보관된 시스템에 대한 접근통제
              </p>

              <h5>제8조 (개인정보 자동 수집 장치의 설치·운영 및 거부에 관한 사항)</h5>
              <p>
                ① 회사는 이용자에게 맞춤형 서비스를 제공하기 위해 이용정보를 저장하고 수시로 불러오는 ‘쿠키(cookie)’를 사용할 수 있습니다.
                ② 이용자는 웹브라우저 설정을 통해 쿠키 저장을 거부할 수 있으며, 이 경우 일부 서비스 이용에 어려움이 발생할 수 있습니다.
              </p>

              <h5>제9조 (개인정보 보호책임자)</h5>
              <p>
                회사는 개인정보 처리에 관한 업무를 총괄해서 책임지고, 개인정보 처리와 관련한 정보주체의 불만 처리 및 피해구제 등을 위하여
                아래와 같이 개인정보 보호책임자를 지정하고 있습니다.
              </p>
              <p>
                ▶ 개인정보 보호책임자<br />
                성명 : 오유주<br />
                직책 : 대표<br />
                연락처 : sales.focusagents@gmail.com
              </p>

              <h5>제10조 (권익침해 구제 방법)</h5>
              <p>정보주체는 아래의 기관에 개인정보 침해에 대한 피해구제, 상담 등을 문의할 수 있습니다.</p>
              <p>
                ▶ 개인정보 침해신고센터 (한국인터넷진흥원) : (국번없이) 118 / privacy.kisa.or.kr<br />
                ▶ 개인정보 분쟁조정위원회 : 1833-6972 / www.kopico.go.kr<br />
                ▶ 대검찰청 사이버수사과 : (국번없이) 1301 / www.spo.go.kr<br />
                ▶ 경찰청 사이버수사국 : (국번없이) 182 / ecrm.police.go.kr
              </p>

              <h5>제11조 (개인정보 처리방침의 변경)</h5>
              <p>이 개인정보 처리방침은 2026. 7. 16.부터 적용됩니다.</p>
            </div>
          </div>
        </div>
      )}
    </>
  )
}
